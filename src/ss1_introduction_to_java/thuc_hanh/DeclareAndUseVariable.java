package ss1_introduction_to_java.thuc_hanh;

public class DeclareAndUseVariable {
    public static void main(String[] args) {
        int i = 10;
        float f = 20.5f;
        double d = 20.5;
        boolean b = true;
        char c = 'a';
        String s = "Hà Nội";

        System.out.println("giá trị của i là: " + i);
        System.out.println("giá trị của f là: " + f);
        System.out.println("giá trị của d là: " + d);
        System.out.println("giá trị của b là: " + b);
        System.out.println("giá trị của c là: " + c);
        System.out.println("giá trị của s là: " + s);

    }
}
